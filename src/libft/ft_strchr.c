#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == (char)c)
			break ;
		i++;
	}
	if (str[i] != '\0' || c == '\0')
		return ((char *)(str + i));
	return (NULL);
}
